<?php
declare(strict_types=1);
/**
 * Writes vendor/.htaccess after composer install (vendor/ is gitignored).
 */
$vendor = dirname(__DIR__) . DIRECTORY_SEPARATOR . 'vendor';
if (!is_dir($vendor)) {
    exit(0);
}
$line = "# Deny HTTP access to Composer packages (auto-generated by composer post-install)\n";
$line .= "Require all denied\n";
file_put_contents($vendor . DIRECTORY_SEPARATOR . '.htaccess', $line);
